generateSpawnPoint = function(){
    point = [Math.floor(Math.random()*mapHeight + mapMargin),
             Math.floor(Math.random()*mapWidth + mapMargin)];
    if (map[point[0]][point[1]] == '.'){
      return point;
    } else {
      return generateSpawnPoint();
    }
}
